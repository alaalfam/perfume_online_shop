/* ---------- Store-backed articles ---------- */
window.MehrabArticles = (function (store, seedArticles) {
  "use strict";

  const KEY = "articles";
  store.seed(KEY, seedArticles);

  function articles() { return store.get(KEY, []); }

  function all() {
    return articles().slice().sort(function (a, b) { return b.date.localeCompare(a.date); });
  }

  function getBySlug(slug) {
    return articles().find(function (a) { return a.slug === slug; }) || null;
  }

  function getRelated(slug, limit) {
    const list = articles();
    const current = getBySlug(slug);
    if (!current) return list.slice(0, limit || 3);
    const sameCategory = list.filter(function (a) { return a.slug !== slug && a.category === current.category; });
    const rest = list.filter(function (a) { return a.slug !== slug && a.category !== current.category; });
    return sameCategory.concat(rest).slice(0, limit || 3);
  }

  /* ---------- Admin CRUD ---------- */
  function create(article) {
    const list = articles();
    if (!article.slug) article.slug = store.slugify(article.title, "article");
    if (!article.isoDate) article.isoDate = new Date().toISOString().slice(0, 10);
    if (!article.date) {
      article.date = article.isoDate; /* admin-created articles show the ISO date if no Jalali date was given */
    }
    list.push(article);
    store.set(KEY, list);
    return article;
  }

  function update(slug, patch) {
    const list = articles();
    const idx = list.findIndex(function (a) { return a.slug === slug; });
    if (idx === -1) return null;
    list[idx] = Object.assign({}, list[idx], patch, { slug: list[idx].slug });
    store.set(KEY, list);
    return list[idx];
  }

  function remove(slug) {
    const list = articles().filter(function (a) { return a.slug !== slug; });
    store.set(KEY, list);
  }

  return {
    all: all,
    getBySlug: getBySlug,
    getRelated: getRelated,
    create: create,
    update: update,
    remove: remove
  };
})(window.MehrabStore, window.MEHRAB_ARTICLES);
